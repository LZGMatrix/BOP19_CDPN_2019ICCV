class UPnP(torch.nn.Module):

    def __init__(self):
        super(UPnP, self).__init__()

    def forward(self, obj_points, proj_points, K):

        assert obj_points.dtype == K.dtype
        assert proj_points.dtype == K.dtype
        assert len(obj_points) == len(proj_points)
        assert K.shape == torch.Size([3, 3])
        N = len(proj_points)
        assert N >= 4  # We only support the symmetric case

        # Build bearing vectors: homogenize, inverse K, and normalize!
        K_inv = K.inverse()
        homo_ones = obj_points.new_ones(N, 1, dtype=K.dtype, device=K.device)
        homo_points = torch.cat((proj_points, homo_ones), dim=1)
        bearing = (K_inv @ homo_points.t()).t()
        bearing = bearing / bearing.norm(dim=1, keepdim=True)

        # Bearing line projection
        F = torch.stack([torch.ger(f, f) for f in bearing]).sum(0)

        # H matrices
        EYE = torch.eye(3, dtype=K.dtype, device=K.device)
        H_inv = (len(bearing) * EYE) - F
        H = H_inv.inverse()

        # Build phi matrix from each 3D point
        phis = [
            torch.tensor(
                [
                    [x, x, -x, -x, 0.0, 2.0 * z, -2.0 * y, 2.0 * y, 2.0 * z, 0.0],
                    [y, -y, y, -y, -2.0 * z, 0.0, 2.0 * x, 2.0 * x, 0.0, 2.0 * z],
                    [z, -z, -z, z, 2.0 * y, -2.0 * x, 0.0, 0.0, 2.0 * x, 2.0 * y],
                ],
                dtype=K.dtype,
                device=K.device,
            ) for (x, y, z) in obj_points
        ]

        # Build FtF-I for each bearing vector
        FtFI = [torch.ger(f, f) - EYE for f in bearing]

        # I matrix
        I = torch.zeros(3, 10, dtype=K.dtype, device=K.device)
        for f, phi in zip(FtFI, phis):
            I += torch.matmul(torch.matmul(H, f), phi)

        # We solve the centralized absolute pose
        M = torch.zeros(10, 10, dtype=K.dtype, device=K.device)
        for f, phi in zip(FtFI, phis):
            Ai = torch.matmul(f, phi + I)
            M += torch.matmul(Ai.t(), Ai)

        # Build action matrix for symmetric case (for n > 4 points)
        action = setup_action_matrix_symm(M)

        # Decompose action matrix to return all 8 possible rotational solutions
        w, v = torch.eig(action, eigenvectors=True)

        # Get unscaled quaternions and normalize each
        quats = v[4:8].t()
        quats = quats / torch.norm(quats, dim=1, keepdim=True)

        # Now build the solutions and measure the error for each
        solutions = []
        for q in quats:
            # Build Rpi multinomial vector from quaternion
            s = torch.tensor(
                [
                    q[0]**2,
                    q[1]**2,
                    q[2]**2,
                    q[3]**2,
                    q[0] * q[1],
                    q[0] * q[2],
                    q[0] * q[3],
                    q[1] * q[2],
                    q[1] * q[3],
                    q[2] * q[3],
                ],
                dtype=K.dtype,
                device=K.device,
            )

            # Compute object space error over all points
            error = s.matmul(M).matmul(s).item()

            # Build the translation and rotation
            t = I.matmul(s)
            R = torch.eye(3, dtype=K.dtype, device=K.device)
            R[0, 0] = q[0]**2 + q[1]**2 - q[2]**2 - q[3]**2
            R[0, 1] = 2.0 * (q[1] * q[2] - q[0] * q[3])
            R[0, 2] = 2.0 * (q[1] * q[3] + q[0] * q[2])
            R[1, 0] = 2.0 * (q[1] * q[2] + q[0] * q[3])
            R[1, 1] = q[0]**2 - q[1]**2 + q[2]**2 - q[3]**2
            R[1, 2] = 2.0 * (q[2] * q[3] - q[0] * q[1])
            R[2, 0] = 2.0 * (q[1] * q[3] - q[0] * q[2])
            R[2, 1] = 2.0 * (q[2] * q[3] + q[0] * q[1])
            R[2, 2] = q[0]**2 - q[1]**2 - q[2]**2 + q[3]**2
            solutions.append([error, R, t])

        # Sort all results with ascending error
        solutions.sort(key=lambda tup: tup[0])
        # NOTE: We always return the solution with lowest error
        # NOTE: Chirality & second-order optimality check maybe better
        return solutions
