def gauss_jordan(M, exit_row):

    precision = 1.0e-10
    rows, cols = M.shape
    for frontrow in range(rows):

        # Find the row that has the biggest leading coefficient
        submatrix = np.abs(M.detach()[frontrow:, frontrow])
        maxrow = np.unravel_index(np.argmax(submatrix), submatrix.shape)[0]

        # Account for submatrix shift from 0
        pivot_row = maxrow + frontrow

        # rowIter is now the row that should go in the place of frontRow->swap
        # Numpy slicing doesn't copy, so use tuple indexing
        # indices = torch.arange(rows)
        # indices[[maxrow, frontrow]] = indices[[frontrow, maxrow]]

        if False:
            E = torch.eye(rows).double()
            E[pivot_row, pivot_row], E[frontrow, frontrow] = 0, 0
            E[pivot_row, frontrow], E[frontrow, pivot_row] = 1, 1
            M = E.matmul(M)
        else:
            # Numpy slicing doesn't copy, so use tuple indexing
            M[[pivot_row, frontrow]] = M[[frontrow, pivot_row]]

        # First divide all coefficients by the leading coefficient
        if False:
            E = torch.eye(rows).double()
            E[frontrow, frontrow:] /= M[frontrow, frontrow]
            M = E.matmul(M)
        else:
            factor = 1 / M[frontrow, frontrow].item()
            M[frontrow, frontrow:] *= factor  # M[frontrow, frontrow]

        # Determine all columns in frontrow that need manipulation
        nnz = M.detach().abs()[frontrow] > precision

        # iterate through all remaining rows, and subtract correct multiple of
        # first row (if leading coefficient is non-zero!)
        for row in range(frontrow + 1, rows):
            leadingCoeff = M[row, frontrow]
            if abs(leadingCoeff) > precision:
                M[row, nnz] -= leadingCoeff * M[frontrow, nnz]

    # Now step up from last non-zero row
    for workrow in range(rows - 1, exit_row, -1):

        # Check which columns needs manipulation
        nnz = M.detach().abs()[workrow, workrow:] > precision

        # Work down the rows
        for row in range(workrow - 1, exit_row, -1):

            # Get the leading coefficient
            leadingCoeff = M[row, workrow]

            # Subtract the multiplied front-row
            if abs(leadingCoeff) > precision:
                M[row, workrow:][nnz] -= leadingCoeff * M[workrow, workrow:][nnz]

    return M


